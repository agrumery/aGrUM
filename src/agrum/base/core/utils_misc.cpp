#include <agrum/base/core/utils_misc.h>

namespace std {
  std::ostream& operator<<(std::ostream& os, const std::tuple<>&) { return os << "()"; }
}   // namespace std

namespace gum {

  bool Memcmp(const void* const in_, const void* const out_, unsigned long size) {
    unsigned char* in  = (unsigned char*)in_;
    unsigned char* out = (unsigned char*)out_;

    for (unsigned long i = 0; i < size; ++i)
      if (*(in++) != *(out++)) return false;

    return true;
  }

  void _atexit_() {
#ifdef GUM_DEBUG_MODE
    gum::__debug__::_atexit_();
#endif
  }

} /* namespace gum */
